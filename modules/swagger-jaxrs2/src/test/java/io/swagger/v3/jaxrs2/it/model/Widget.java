package io.swagger.v3.jaxrs2.it.model;

public class Widget {

    private String a;
    private String b;
    private String id;

    public String getA() {
        return a;
    }

    public Widget setA(String a) {
        this.a = a;
        return this;
    }

    public String getB() {
        return b;
    }

    public Widget setB(String b) {
        this.b = b;
        return this;
    }

    public String getId() {
        return id;
    }

    public Widget setId(String id) {
        this.id = id;
        return this;
    }
}
